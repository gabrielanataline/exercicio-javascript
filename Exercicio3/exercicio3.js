//Dado o tamanho do raio de uma circunferência, calcular a área e o perímetro da mesma.

alert('Vamos calcular a área e o perímetro do raio de uma circunfêrencia')

let raio = Number (prompt('Informe o tamanho do raio:'))

let area = 3.14*(raio**2)
let perimetro = raio*(2*3.14)

alert("o tamanho da área é "+area +" e o tamanho do perímetro é " + perimetro+";")






