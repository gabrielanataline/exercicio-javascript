//Dado o tamanho da base e da altura de um retângulo, calcular a sua área e o seu perímetro

alert('BEM VINDO, vamos calcular a área e o perímetro de um retângulo')

let base = Number (prompt('Digite o tamanho da base'))

let altura = Number (prompt('Digite a altura'))

let resultadoarea = base*altura

let resultadoperimetro = 2 * (base + altura) 


alert("Resultado da área é: " + resultadoarea+ " e o Resultado do perímetro é: " + resultadoperimetro)


